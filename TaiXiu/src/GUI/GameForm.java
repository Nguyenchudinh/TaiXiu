    private void GuiTinNhanTong() {
        String s=txtTinNhan.getText();
        if(!txtTinNhan.getText().isEmpty())
        {
            Date d = new Date();
            SimpleDateFormat format= new SimpleDateFormat("hh:mm");
            areaChat.append("["+format.format(d)+"] Tôi: "+s+"\n");
            txtTinNhan.setText("");
            bus.GuiTinNhan(s);
        }
    }

    private void GuiTinNhanCaNhan() {
        if(labelTNten.getText()=="Chọn 1 người để chat")
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một người hoặc click button bên cạnh và nhập tên để bắt đầu chat.");
        else if(!txtTN.getText().isEmpty())
        {
            String nguoinhan=labelTNten.getText();
            String tinnhan=txtTN.getText();
            Date d = new Date();
            SimpleDateFormat format= new SimpleDateFormat("hh:mm");
            areaTN.append("["+format.format(d)+"] Tôi: "+tinnhan+"\n");
            txtTN.setText("");
            bus.GuiTinNhanCaNhan(nguoinhan,tinnhan);
        }
    }

