    private void DatCuocTai()
    {
        if(txtTai.getText().isEmpty()) JOptionPane.showMessageDialog(this, "Chưa nhập số tiền");
        else if(labelTienXiu.getText()!="0") JOptionPane.showMessageDialog(this, "Không thể đặt cược một lúc cả Tài và Xỉu");
        else
        {
            Double tiendat=Double.parseDouble(txtTai.getText());
            Double tiendadat=Double.parseDouble(labelTienTai.getText());
            Double tien=Double.parseDouble(labelTien.getText());
            if(tiendat <= tien)
            {
                tien=tien+tiendadat-tiendat;
                labelTienTai.setText(tiendat.toString());
                labelTien.setText(tien.toString());
                txtTai.setText("");
            }
            else JOptionPane.showMessageDialog(this, "Không đủ tiền","Thông báo",JOptionPane.OK_OPTION);
        }
    }
